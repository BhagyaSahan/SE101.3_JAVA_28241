package com.mycompany.javapractical01;

public class JavaPractical01
{
    public static void main(String[] args)
    {
        for(int i=0; i<5; i++)
        {
            System.out.println("Executing Loop "+i);
        }
        
        int k = 0;
        while(k<5)
        {
            System.out.println("Executing Loop "+k);
            k++;
        }
    }
}
